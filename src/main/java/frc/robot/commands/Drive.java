package frc.robot.commands;

import edu.wpi.first.wpilibj.XboxController;
import edu.wpi.first.wpilibj.GenericHID.Hand;
import edu.wpi.first.wpilibj.command.Command;
import frc.robot.Robot;
import frc.robot.subsystems.DriveTrain;

public class Drive extends Command {
  XboxController controller;
  public Drive(XboxController controller) {
    requires(Robot.driveTrainSubsystem);
    this.controller = controller;
    // Use requires() here to declare subsystem dependencies
    // eg. requires(chassis);
  }

  // Called just before this Command runs the first time
  @Override
  protected void initialize() {
  }

  // Called repeatedly when this Command is scheduled to run
  @Override
  protected void execute() {
    double forwardsSpeed = controller.getY(Hand.kLeft);
    double sidewaysSpeed = controller.getX(Hand.kLeft);
    double rotation = controller.getX(Hand.kRight);
    Robot.driveTrainSubsystem.drive(forwardsSpeed, sidewaysSpeed, rotation);
  }

  // Make this return true when this Command no longer needs to run execute()
  @Override
  protected boolean isFinished() {
    return false;
  }

  // Called once after isFinished returns true
  @Override
  protected void end() {
  }

  // Called when another command which requires one or more of the same
  // subsystems is scheduled to run
  @Override
  protected void interrupted() {
  }
}
